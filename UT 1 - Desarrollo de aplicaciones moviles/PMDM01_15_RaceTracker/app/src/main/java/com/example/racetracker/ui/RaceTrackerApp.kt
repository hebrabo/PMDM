@Composable
fun RaceTrackerApp() {
    /**
     * REMEMBER:
     * Usamos 'remember' para que los objetos RaceParticipant sobrevivan a la recomposición.
     * Si no usáramos 'remember', cada vez que la pantalla se dibuje, se crearían nuevos jugadores
     * y el progreso se perdería.
     */
    val playerOne = remember {
        RaceParticipant(name = "Player 1", progressIncrement = 1)
    }
    val playerTwo = remember {
        RaceParticipant(name = "Player 2", progressIncrement = 2)
    }

    // Estado booleano que controla si la carrera está activa o no.
    var raceInProgress by remember { mutableStateOf(false) }

// Comprobamos la variable de estado. Si es 'true', entramos al bloque.
    if (raceInProgress) {
        LaunchedEffect(playerOne, playerTwo) {
            // coroutineScope crea un ámbito para lanzar múltiples tareas hijas
            coroutineScope {
                // 'launch' dispara la tarea y CONTINÚA INMEDIATAMENTE a la siguiente línea
                // sin esperar a que termine.
                launch { playerOne.run() } // Arranca el hilo del Jugador 1
                launch { playerTwo.run() } // Arranca el hilo del Jugador 2 INMEDIATAMENTE
            }
            // coroutineScope se queda esperando aquí hasta que TODOS sus hijos (launch) terminen.

            // Una vez que ambos han llegado a la meta, se ejecuta esto:
            raceInProgress = false
        }
    }

    // Pantalla principal
    RaceTrackerScreen(
        playerOne = playerOne,
        playerTwo = playerTwo,
        isRunning = raceInProgress,
        // STATE HOISTING (Elevación de estado):
        // Pasamos una función lambda para que el hijo pueda pedirle al padre que cambie el estado.
        onRunStateChange = { raceInProgress = it },
        modifier = Modifier
            .statusBarsPadding()
            .fillMaxSize()
            .verticalScroll(rememberScrollState())
            .safeDrawingPadding()
            .padding(horizontal = dimensionResource(R.dimen.padding_medium)),
    )
}

@Composable
private fun RaceTrackerScreen(
    playerOne: RaceParticipant,
    playerTwo: RaceParticipant,
    isRunning: Boolean,
    onRunStateChange: (Boolean) -> Unit, // Callback para modificar el estado
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier,
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Text(
            text = stringResource(R.string.run_a_race),
            style = MaterialTheme.typography.headlineSmall,
        )
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(dimensionResource(R.dimen.padding_medium)),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally,
        ) {
            // Icono decorativo
            Icon(
                painter = painterResource(R.drawable.ic_walk),
                contentDescription = null,
                modifier = Modifier.padding(dimensionResource(R.dimen.padding_medium)),
            )

            // Indicador Jugador 1
            // Observa cómo leemos 'playerOne.currentProgress'. Como es un State,
            // cuando cambie, esta función se volverá a ejecutar (recomposición)
            // y la barra se moverá.
            StatusIndicator(
                participantName = playerOne.name,
                currentProgress = playerOne.currentProgress,
                maxProgress = stringResource(
                    R.string.progress_percentage,
                    playerOne.maxProgress
                ),
                progressFactor = playerOne.progressFactor,
                modifier = Modifier.fillMaxWidth()
            )

            Spacer(modifier = Modifier.size(dimensionResource(R.dimen.padding_large)))

            // Indicador Jugador 2
            StatusIndicator(
                participantName = playerTwo.name,
                currentProgress = playerTwo.currentProgress,
                maxProgress = stringResource(
                    R.string.progress_percentage,
                    playerTwo.maxProgress
                ),
                progressFactor = playerTwo.progressFactor,
                modifier = Modifier.fillMaxWidth(),
            )

            Spacer(modifier = Modifier.size(dimensionResource(R.dimen.padding_large)))

            // Botones de control
            RaceControls(
                isRunning = isRunning,
                onRunStateChange = onRunStateChange,
                onReset = {
                    playerOne.reset()
                    playerTwo.reset()
                    onRunStateChange(false)
                },
                modifier = Modifier.fillMaxWidth(),
            )
        }
    }
}

@Composable
private fun StatusIndicator(
    participantName: String,
    currentProgress: Int,
    maxProgress: String,
    progressFactor: Float,
    modifier: Modifier = Modifier
) {
    Row(modifier = modifier) {
        Text(
            text = participantName,
            modifier = Modifier.padding(end = dimensionResource(R.dimen.padding_small))
        )
        Column(
            verticalArrangement = Arrangement.spacedBy(dimensionResource(R.dimen.padding_small))
        ) {
            // COMPONENTE DE UI: Barra de progreso lineal
            LinearProgressIndicator(
                progress = { progressFactor }, // Se actualiza automáticamente
                modifier = Modifier
                    .fillMaxWidth()
                    .height(dimensionResource(R.dimen.progress_indicator_height))
                    .clip(RoundedCornerShape(dimensionResource(R.dimen.progress_indicator_corner_radius))),
                color = ProgressIndicatorDefaults.linearColor,
                trackColor = ProgressIndicatorDefaults.linearTrackColor,
                strokeCap = ProgressIndicatorDefaults.LinearStrokeCap,
            )
            // Fila con los textos de porcentaje (Ej: "45%")
            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.SpaceBetween
            ) {
                Text(
                    text = stringResource(R.string.progress_percentage, currentProgress),
                    textAlign = TextAlign.Start,
                    modifier = Modifier.weight(1f)
                )
                Text(
                    text = maxProgress,
                    textAlign = TextAlign.End,
                    modifier = Modifier.weight(1f)
                )
            }
        }
    }
}

@Composable
private fun RaceControls(
    onRunStateChange: (Boolean) -> Unit,
    onReset: () -> Unit,
    modifier: Modifier = Modifier,
    isRunning: Boolean = true,
) {
    Column(
        modifier = modifier.padding(top = dimensionResource(R.dimen.padding_medium)),
        verticalArrangement = Arrangement.spacedBy(dimensionResource(R.dimen.padding_medium))
    ) {
        // Botón Iniciar / Pausar
        Button(
            // Al hacer clic, invertimos el valor booleano actual
            onClick = { onRunStateChange(!isRunning) },
            modifier = Modifier.fillMaxWidth(),
        ) {
            Text(if (isRunning) stringResource(R.string.pause) else stringResource(R.string.start))
        }

        // Botón Reiniciar
        OutlinedButton(
            onClick = onReset,
            modifier = Modifier.fillMaxWidth(),
        ) {
            Text(stringResource(R.string.reset))
        }
    }
}

